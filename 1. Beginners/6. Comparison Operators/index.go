package main

import "fmt"

func main() {
	numOne := 10
	numTwo := 20
	fmt.Println(numOne > numTwo)
	fmt.Println(numOne < numTwo)
	fmt.Println(numOne >= numTwo)
	fmt.Println(numOne <= numTwo)
	fmt.Println(numOne == numTwo)
	fmt.Println(numOne != numTwo)

	fmt.Println(2 > 2)
	fmt.Println(2 < 2)
	fmt.Println(2 >= 2)
	fmt.Println(2 <= 2)
	fmt.Println(2 == 2)
	fmt.Println(2 != 2)
}
